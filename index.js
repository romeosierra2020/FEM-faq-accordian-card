const pointer1 = document.getElementById("pointer1");
const question1 = document.getElementById("question1");
const answer1 = document.getElementById("answer1");
question1.addEventListener('click', function() {
    clicked1();
});

function clicked1() {
    pointer1.classList.toggle('pointer');
    pointer1.classList.toggle('pointer-open');
    question1.classList.toggle('question');
    question1.classList.toggle('question-open');
    answer1.classList.toggle('answer');
    answer1.classList.toggle('answer-open');
}

